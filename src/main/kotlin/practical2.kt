fun main(){
    var i: Int = 98
    println("Integer Value : $i")
    var a = i.toDouble()
    println("Double Value (From Integer) : $a")
    var s: String = "98"
    println("String Value : $s")
    var b = s.toInt()
    println("Integer Value1 : $b")
    var d: String = "10"
    println("String Value : $d")
    var c = d.toDouble()
    println("Double Value (From String) : $c")
}